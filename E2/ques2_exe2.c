#include <stdio.h>
#include <stdlib.h>

int main()
{
    int row, c, n, temp;

   printf("Enter the rows ");
   scanf("%d",&n);

   temp = n;

   for ( row = 1 ; row <= n ; row++ )
   {
      for ( c = 1 ; c < temp ; c++ )
      temp;

      for ( c = 1 ; c <= row ; c++ )
         printf("9");

      printf("\n");
   }

    return 0;
}
